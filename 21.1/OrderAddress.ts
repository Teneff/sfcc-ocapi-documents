export interface OrderAddress {
  /**
   * The first address.
   */
  address1: string;
  /**
   * The second address.
   */
  address2: string;
  /**
   * The city.
   */
  city: string;
  /**
   * The company name.
   */
  company_name: string;
  /**
   * The two-letter ISO 3166-1 (Alpha-2) country code.
   */
  country_code: string;
  /**
   * The first name.
   */
  first_name: string;
  /**
   * The full name.
   */
  full_name: string;
  /**
   * Id used to identify this address
   */
  id: string;
  /**
   * The job title.
   */
  job_title: string;
  /**
   * The last name.
   */
  last_name: string;
  /**
   * The phone number.
   */
  phone: string;
  /**
   * The post box.
   */
  post_box: string;
  /**
   * The postal code.
   */
  postal_code: string;
  /**
   * The salutation.
   */
  salutation: string;
  /**
   * The second name.
   */
  second_name: string;
  /**
   * The state code.
   */
  state_code: string;
  /**
   * The suffix.
   */
  suffix: string;
  /**
   * The suite.
   */
  suite: string;
  /**
   * The title.
   */
  title: string;
}
